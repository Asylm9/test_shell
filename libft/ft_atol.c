#include "libft.h"

long	ft_atol(const char *str)
{
	int		i;
	long	neg;
	long	result;

	i = 0;
	neg = 1;
	result = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] > 47 && str[i] < 58)
		result = (result * 10) + (str[i++] - '0');
	return (result * neg);
}
